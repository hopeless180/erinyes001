package cc.erinyes.model;

public class GXinfo {
	private int id;
	private String name;
	private String info;
	
	public int getid(){
		return id;
	}
	public void setid(int id){
		this.id = id;
	}
	public String getname(){
		return name;
	}
	public void setname(String name){
		this.name = name;
	}
	public String getinfo(){
		return info;
	}
	public void setinfo(String info){
		this.info = info;
	}
}
